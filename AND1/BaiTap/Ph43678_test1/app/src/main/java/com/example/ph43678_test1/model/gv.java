package com.example.ph43678_test1.model;

public class gv {
    private String maGv;
    private String hoTen;

    public gv() {
    }

    public gv(String maGv, String hoTen) {
        this.maGv = maGv;
        this.hoTen = hoTen;
    }

    public String getMaGv() {
        return maGv;
    }

    public void setMaGv(String maGv) {
        this.maGv = maGv;
    }

    public String getHoTen() {
        return hoTen;
    }

    public void setHoTen(String hoTen) {
        this.hoTen = hoTen;
    }
}
